const month = 3
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march"); //"march"
        break;
    case 4:
        console.log("april");
        break;

    default:
        console.log("default case matched");
        break;
}

//Note: once a key got matched it will execute the further code, except default. That is why break is imp.
